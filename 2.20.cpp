#include <iostream>
using namespace std;

int main()
{
    int r;
    int dia;
    int circu;
    int area;
    int pi;
    cin>>r;
    pi = 3.14159;
    dia = r*2;
    circu = 2*pi*r;
    area = pi*(r*r);
    cout<<"El diametro es (r2) :"<< dia<<endl;
    cout<<"La circunferencia es (2PIr) :"<< circu<<endl;
    cout<<"El area es (pi(r*r) :) :"<< area<<endl;




}